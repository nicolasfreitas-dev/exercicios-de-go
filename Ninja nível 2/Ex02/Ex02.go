package main

import (
	"fmt"
)

func main() {
	a := 1
	b := 2
	c := 3
	d := 4
	e := 5

	fmt.Println(a < b)
	fmt.Println(b <= e)
	fmt.Println(c == a)
	fmt.Println(b > a)
	fmt.Println(d != c)
	fmt.Println(c >= a)
}